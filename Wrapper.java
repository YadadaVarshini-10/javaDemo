public class Wrapper {

  public static void main(String[] args) {
    
    int num=7;
    Integer num1=new Integer(num);
    System.out.println(num1);

    //Boxing
    int num2=6;
    Integer num3=new Integer(num2);
    System.out.println(num3);

    //auto-boxing
    int num4=7;
    Integer num5=num4;
    System.out.println(num5);

  
   

  }
  
}
